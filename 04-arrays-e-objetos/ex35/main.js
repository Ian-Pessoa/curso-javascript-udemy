const calculadora = {
  somar(num1, num2) {
    console.log(num1 + num2);
  },
  subtrair(num1, num2) {
    console.log(num1 - num2);
  },
  multiplicar(num1, num2) {
    console.log(num1 * num2);
  },
  dividir(num1, num2) {
    console.log(num1 / num2);
  }
}

calculadora.somar(1, 2);
calculadora.subtrair(10, 5);
calculadora.multiplicar(5, 4);
calculadora.dividir(10, 2);
